// Stage real "AGI Awareness Post" front pages for the montage.
//
// The newspaper PNGs already live in the repo at research/front-page/*.png as
// regular git blobs (NOT LFS — see .gitattributes). Rather than committing a
// duplicate set into the promo, we copy a recent spread into public/frontpages/
// (gitignored) at build time and emit src/frontpages.ts as the single source of
// truth for which editions the montage renders. Reproducible from a clean
// clone because research/front-page/ ships with the repo.
import {readdirSync, statSync, mkdirSync, copyFileSync, writeFileSync, rmSync} from 'node:fs';
import {fileURLToPath} from 'node:url';
import {dirname, join} from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const promoRoot = join(__dirname, '..');
const repoRoot = join(promoRoot, '..');
const srcDir = join(repoRoot, 'research', 'front-page');
const dstDir = join(promoRoot, 'public', 'frontpages');

const COUNT = 12; // how many recent editions the montage fans through

const DATE_RE = /^(\d{4}-\d{2}-\d{2})-front-page\.png$/;

const editions = readdirSync(srcDir)
  .map((f) => {
    const m = f.match(DATE_RE);
    if (!m) return null;
    const full = join(srcDir, f);
    const size = statSync(full).size;
    if (size < 1024) return null; // skip 0-byte / stub renders
    return {date: m[1], file: f, full};
  })
  .filter(Boolean)
  .sort((a, b) => (a.date < b.date ? 1 : -1)) // newest first
  .slice(0, COUNT);

if (editions.length === 0) {
  throw new Error(`No front-page PNGs found in ${srcDir}`);
}

rmSync(dstDir, {recursive: true, force: true});
mkdirSync(dstDir, {recursive: true});
for (const e of editions) {
  copyFileSync(e.full, join(dstDir, `${e.date}.png`));
}

const list = editions.map((e) => e.date);
writeFileSync(
  join(promoRoot, 'src', 'frontpages.ts'),
  `// AUTO-GENERATED by scripts/copy-frontpages.mjs — do not edit by hand.\n` +
    `// The ${list.length} most recent "AGI Awareness Post" editions, newest first.\n` +
    `// PNGs are staged (gitignored) in public/frontpages/ from research/front-page/.\n` +
    `export const EDITIONS = ${JSON.stringify(list, null, 2)} as const;\n`,
);

console.log(`copy-frontpages: staged ${list.length} editions (${list[0]} … ${list[list.length - 1]})`);
